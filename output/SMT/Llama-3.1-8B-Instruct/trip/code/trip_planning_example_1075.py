# Define the constraints
solver = Solver()

# Define the variables
x = {city: [Bool(f'{city}_day_{day}') for day in days] for city in cities}

# Constraints for annual show
for day in range(5, 9):
    solver.add(Not(x['Edinburgh'][day]))

# Constraints for wedding
for day in range(19, 24):
    solver.add(Not(x['Split'][day]))

# Constraints for staying in each city
for city in cities:
    for day in days:
        if city == 'Vienna' and (day >= 1 and day <= 4):
            solver.add(x[city][day])
        elif city == 'Lyon' and (day >= 1 and day <= 3):
            solver.add(x[city][day])
        elif city == 'Edinburgh' and (day >= 1 and day <= 4):
            solver.add(x[city][day])
