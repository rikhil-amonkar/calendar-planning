# Define the constraints
solver = Solver()

# Define the variables
x = {city: [Bool(f'{city}_day_{day}') for day in days] for city in cities}

# Constraints for workshop
for day in [1, 2, 3]:
    solver.add(Not(x['Porto'][day]))

# Constraints for wedding
for day in [13, 14, 15]:
    solver.add(Not(x['Warsaw'][day]))

# Constraints for staying in each city
for city in cities:
    for day in days:
        if city == 'Paris' and (day >= 1 and day <= 5):
            solver.add(x[city][day])
        elif city == 'Florence' and (day >= 1 and day <= 3):
            solver.add(x[city][day])
        elif city == 'Vienna' and (day >= 1 and day <= 2):
            solver.add(x[city][day])
        elif city == 'Porto' and (day >= 1 and day <= 3):
            solver.add(x[city][day])
        elif city == 'Munich' and (day >= 1 and day <= 5):
            solver.add(x[city][day])
        elif city == 'Nice' and (day >= 1 and day <= 5):
            solver.add(x[city][day])
        elif city == 'Warsaw' and (day >= 1 and day <= 3):
            solver.add(x[city][day])

# Constraints for direct flights
for (city1, city2), days in flights.items():
    for day in days:
        solver.add(Implies(x[city1][day], x[city2][day]))

# Solve the problem
if solver.check() == sat:
    model = solver.model()
    trip_plan = {}
    for city in cities:
        trip_plan[city] = []
        for day in days:
            if model[x[city][day]]:
                trip_plan[city].append(day)
    print(trip_plan)
else:
    print("No solution exists")